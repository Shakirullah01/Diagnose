def resolve_parent_result_status(per_category_status: dict[str, str]) -> str:
    """normal if all filled statuses are 'норма'; otherwise delayed."""
    statuses = [s for s in (per_category_status or {}).values() if s]
    if statuses and all(s == "норма" for s in statuses):
        return "normal"
    return "delayed"


